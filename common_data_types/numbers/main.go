package main

import (
	"fmt"
	"math"
)

func main() {

	fmt.Println("Addition:", 2+8)
	fmt.Println("Substruction:", 2-8)
	fmt.Println("Multiplication:", 2*8)

	fmt.Println("Division:", 2/8)
	fmt.Println("Division:", 8/2)
	fmt.Println("Division:", 2.0/8)
	fmt.Println("Division:", 20/6)
	fmt.Println("Division:", 20/6.0)

	fmt.Println("Exponents: ", math.Pow(7, 3))
}
